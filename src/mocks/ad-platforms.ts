interface AdSet {
  id: string;
  name: string;
  budget: number;
  spend: number;
  impressions: number;
  clicks: number;
  conversions: number;
}

interface AdGroup {
  id: string;
  name: string;
  budget: number;
  spend: number;
  impressions: number;
  clicks: number;
  conversions: number;
}

export interface AdCampaign {
  id: string;
  name: string;
  platform: "google" | "meta";
  status: "active" | "completed" | "paused";
  spend: number;
  impressions: number;
  clicks: number;
  conversions: number;
  ctr: number; // Click-through rate (%)
  cpc: number; // Cost per click (€)
  cpm: number; // Cost per mille (€)
  startDate: Date;
  endDate?: Date;
  adSets?: AdSet[]; // For Meta
  adGroups?: AdGroup[]; // For Google
}

